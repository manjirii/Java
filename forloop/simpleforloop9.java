//Write a program to print the sum of the first 10 numbers

public class simpleforloop9 {

    public static void main(String[] args) {

        int sum = 0;

        for (int i = 0; i <= 10; i++) {

            sum = sum + i;
        }

        System.out.println("Sum of first 10 Number is " + sum);
    }
}
